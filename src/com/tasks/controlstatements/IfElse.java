package com.tasks.controlstatements;

public class IfElse {

	public static void main(String[] args) {
		
		int num = 1;
		
		if (num == 1) {
			System.out.println("One");
		}else if (num == 2) {
			System.out.println("Two");
		}else if (num == 3) {
			System.out.println("Three");
		}else if (num == 4) {
			System.out.println("Four");
		}else {
			System.out.println("Enter valid input !");
		}
		
	}

}
